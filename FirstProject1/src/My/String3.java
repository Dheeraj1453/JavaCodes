package My;

public class String3 {
	public static void main(String args[]) {
		String st="Dheeraj Kumar Jha";
		System.out.println("Name: "+st);
		System.out.println("In Upper case: "+st.toUpperCase()); //touppercase se string uppercase mai ho jata hai
		System.out.println("In Lower case: "+st.toLowerCase()); //toLowercase se String lower case mai ho jaega
		System.out.println(st.toUpperCase().toLowerCase()); //hmlog 1 hi mai dono operation kr skte hai String mai
		System.out.println(st.charAt(0)); //ye hmlog ko String ka koi 1 character dene k liye use kiya jata hai
	}
	

}
